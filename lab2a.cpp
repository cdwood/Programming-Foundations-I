#include <iostream>
using namespace std;

// Main function
int main()
{
   cout << "Here are some approximations of PI:" << endl;

   // Archimedes 225 BC
   cout << "22 / 7 = " << 22.0 / 7 << endl;

   // Zu Chongzhi 480 AD
   cout << "355 / 113 = " << 355.0 / 113 << endl;

   // Indiana law 1897 AD
   cout << "16 / 5 = " << 16.0 / 5 << endl;

   return 0;
}

//output
/*
Here are some approximations of PI:
22 / 7 = 3.14286
355 / 113 = 3.14159
16 / 5 = 3.2
*/
